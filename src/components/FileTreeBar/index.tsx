function TransitionComponent(props: TransitionProps) {
  const style = useSpring({
    from: { opacity: 0, transform: 'translate3d(20px,0,0)' },
    to: { opacity: props.in ? 1 : 0, transform: `translate3d(${props.in ? 0 : 20}px,0,0)` },
  });

  return (
    <animated.div style={style}>
      <Collapse {...props} />
    </animated.div>
  );
}

const drawerWidth = 240;

const StyledTreeItem = withStyles((theme: Theme) =>
  createStyles({
    iconContainer: {
      '& .close': {
        opacity: 0.3,
      },
    },
    group: {
      marginLeft: 7,
      paddingLeft: 18,
      borderLeft: `1px dashed ${fade(theme.palette.text.primary, 0.4)}`,
    },
  }),
)((props: TreeItemProps) => <TreeItem {...props} TransitionComponent={TransitionComponent} />);

const useStyles = makeStyles((theme: Theme) =>
  createStyles({
    root: {
      height: 264,
      flexGrow: 1,
      maxWidth: 400,
    },
    drawer: {
      width: drawerWidth,
      flexShrink: 0,
    },
    drawerPaper: {
      marginLeft: 60,
      width: drawerWidth,
    },
    margin: {
      margin: theme.spacing(1),
    },
  }),
);

export default function FileTreeBar() {
  const classes = useStyles();
  const [project, updateProject] = useProject();
  const handleNodeSelect = useCallback(
    (event: ChangeEvent, nodeId: any) => {
      updateProject((project) => {
        if (project.fragments[nodeId]) {
          project.selectedFragmentId = nodeId;
        }
        return project;
      });
    },
    [updateProject],
  );

  return (
    <Drawer className={classes.drawer} variant="permanent" classes={{ paper: classes.drawerPaper }}>
      <Toolbar />
      <Divider />
      <div className={classes.margin}>
        <Grid container spacing={1} alignItems="flex-end">
          <Grid item>
            <AccountCircle />
          </Grid>
          <Grid item>
            <TextField id="input-with-icon-grid" label="With a grid" />
          </Grid>
        </Grid>
      </div>
      <TreeView
        className={classes.root}
        selected={[project.selectedFragmentId]}
        defaultExpanded={[project.id]}
        defaultCollapseIcon={<MinusSquare />}
        defaultExpandIcon={<PlusSquare />}
        defaultEndIcon={<CloseSquare />}
        onNodeSelect={handleNodeSelect}
      >
        <StyledTreeItem nodeId={project.id} label={project.name}>
          {Object.values(project.fragments).map((fragment) => (
            <StyledTreeItem
              key={fragment.diagramInfo.id}
              nodeId={fragment.diagramInfo.id}
              label={fragment.diagramInfo.name}
            />
          ))}
        </StyledTreeItem>
      </TreeView>
    </Drawer>
  );
}
